package com.cice.ejercicios;

public class EjercicioFor {

    public static void main(String[] args) {

        //for(inicializacion; condicion; variacion)

        for (int i = 1; i <= 10; i++) {

            System.out.println("2 x "+i+" = "+(2 * i));

        }

        System.out.println("Esto es una cadena que muestra la suma de " + 2 +4 +" con un resultado de "+(2+4));

        System.out.println("cadena " + (2 *4 +6 /3) + " cadena");   // hace primero multiplicaciones y divisiones y luego la suma
    }

}
